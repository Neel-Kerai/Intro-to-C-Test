#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    int x =-10;
    cout<<"Absolute value of " << x << "is" << abs(x) << endl;
    cout<<"Square root of 25 is " << sqrt(25) << endl;
    cout<<"The log of 5 base 10 =" << log(50) << endl;
    cout<<"2^3 =" << pow(2,3) << endl;
    
}